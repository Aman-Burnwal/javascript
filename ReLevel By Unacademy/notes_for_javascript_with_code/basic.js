// order of precendence [when both variable name and function names are same]

// 1. variable assignment takes precedence over function declaration;
    /**
     * var name = "Aman";
     * 
     * function name () {
     *      console.log("Aman");
     * }
     * 
     * console.log(typeof(name))    // here output will be string
     */

// 2. function declararion take precedence over variable declaration
    /**
     * var name;
     * 
     * function name() {
     *    console.log("Aman")
     * }
     * 
     * console.log(typeof(name));   // here output will be function
     */