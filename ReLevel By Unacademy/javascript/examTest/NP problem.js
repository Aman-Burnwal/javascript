// Problem Statement

// Amy calls an array A on N positive integer “good”, if it satisfies all the following condition:

// · Elements of the array A, are non-decreasing, i.e., A1 <= A2 <= … <= AN.

// · Product of all the elements of the array is less than equal to P

// Find how many distinct “good” array A, exists for given value of N and P. 
// Two arrays A and B of length N are said to be different if there exists a K(1 <= K <= N), such that AK is not equal to BK.

// You are given T independent test cases.

// Constraints

// 1 <= T <= 5
// 1 <= N <= 3
// 1 <= P <= 2*10^10
// All input values are integers.
// Input Format

// First-line contains T.
// Next T lines each contains two space separated integers N and P.
// Output Format

// Print in a newline for each test case a single integer denoting the number of distinct “good” array A,
//  that exists for given value of N and P.
// Sample Input 1

// 1

// 2 4

// Sample Output 1

// 5

